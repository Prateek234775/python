# Arithmetic operators
a = 10
b = 3

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)

# Comparison operators
x = 5
y = 10

print(x > y)
print(x < y)
print(x == y)

# if-else
age = int(input("Enter your age: "))

if age >= 18:
    print("You are eligible to vote")
else:
    print("You are not eligible to vote")
